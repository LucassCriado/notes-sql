SELECT B1_COD, B1_DESC, B1_TIPO, B1_GRUPO, B1_UM
FROM [SB1990]
WHERE B1_TIPO = 'PA'
ORDER BY B1_COD DESC; 

SELECT A1_COD, A1_NOME, A1_END, A1_BAIRRO, A1_MUN, A1_EST, A1_CEP
FROM [SA1990]
WHERE NOT
 A1_EST = 'SP'
ORDER BY A1_NOME ASC;

SELECT C6_CLI,A1_COD,A1_EST,C6_ITEM,A1_NOME,C6_DESCRI,C6_PRODUTO,C6_QTDVEN,C6_PRCVEN,C6_VALOR
FROM [SC6990],[SA1990]
WHERE C6_CLI = A1_COD 
AND A1_EST = 'SP'
ORDER BY C6_ITEM, C6_CLI ASC;

SELECT C6_CLI,A1_COD,A1_EST,C6_VALOR
FROM [SC6990],[SA1990]
WHERE C6_CLI = A1_COD
ORDER BY A1_EST, C6_VALOR ASC;
